#include "push_swap.h"

void	sortseq(char *seq, int i, int c)
{
	if (S1)
		++seq[i];
	else if (S2)
		seq[i] += 2;
	else if (S3)
	{
		seq[i] = '2';
		++seq[i - 1];
	}
	while (seq[++i])
		if (seq[i] != '0' && seq[i] != '1')
			seq[i] = seq[i - 1] == '2' ? '3' : '2';
}
