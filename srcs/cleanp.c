#include "push_swap.c"

int		cleanp(char *seq, int i)
{
	while (--i >= 0 && seq[i] != '9')
		;
	i >= 0 ? seq[i] = '0' : 0;
	while (seq[++i] && seq[i] != ':')
		;
	seq[i] ? seq[i] = '0' : 0;
	return (0);
}
