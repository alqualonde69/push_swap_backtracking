#include "push_swap.h"

void	listcpy2(t_ps *src, t_ps **dst)
{
	t_ps	*tmp;

	tmp = *dst;
	while (src && (*dst))
	{
		(*dst)->c = src->c;
		(*dst)->n = src->n;
		*dst = (*dst)->nxt;
		src = src->nxt;
	}
	*dst = tmp;
}
